public class Operators {
    public static void main(String[] args) {
        
        // assignment operators =

        int number = 3;

        //arithmetic operators

        //addition + 

        number = number + 3;
        System.out.println(number);

        //subtraction -
        number = number - 2;
        System.out.println(number);

        
        //multiplication *
        number = number * 4;
        System.out.println(number);

        //division /
        number = number / 2;
        System.out.println(number);

        //modulo &
        number = number % 2;
        System.out.println(number);


    }
}
